# Информация о типе хранится в объекте, но не в переменной

l1=[2,3,4]
l2=l1[:] # Создается копия списка L1
l1[0]=24
print(l1,l2)

import copy
x=copy.copy(l1) # Создание “поверхностной” копии любого объекта Y
print(x)
x=copy.deepcopy(l2) # Создание полной копии: копируются все вложенные части
print(x)

